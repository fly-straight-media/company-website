  /** Enable/disable form submit button. **/

  $(document).ready(function() {
    $('#form-submit-button').attr('disabled', 'disabled');
  });

  const fields = '#name, #email, #message, #privacy-checkbox';

  $(fields).on('keyup change', function() {
    if (allFilled() && isEmailValid()) {
      $('#form-submit-button').removeAttr('disabled');
    } else {
      $('#form-submit-button').attr('disabled', 'disabled');
    }
  });

  function isEmailValid() {
    const regex = /^(([^<>()[\]\\.,;:\s@']+(\.[^<>()[\]\\.,;:\s@']+)*)|('.+'))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
    return regex.test(String($('#email').val()).toLowerCase());
  }

  function allFilled() {
    var filled = true;
    $(fields).each(function() {
      if ($(this).attr('type') === 'checkbox' && !this.checked) {
        filled = false;
        return false; // break
      } else if ($(this).val() == '') {
        filled = false;
        return false; // break
      }
    });
    return filled;
  }

  /* Form submission */

  var request;

  function submitForm() {
    // Abort any pending request
    if (request) {
      request.abort();
    }

    const form = $('#form');
    const inputs = form.find('input, select, button, textarea');
    const serializedData = form.serialize();

    // Let's disable the inputs for the duration of the Ajax request.
    // Note: we disable elements AFTER the form data has been serialized.
    // Disabled form elements will not be serialized.
    inputs.prop('disabled', true);

    // Fire off the request to /mail.php
    request = $.post({
      url: '/mail.php',
      data: serializedData
    });

    request.done(function(response) {
      // We need to extract the JSON part only from the response. 
      // We need this for local dev because Livereload injects it's script into every response.
      const obj = JSON.parse(response.substring(0, response.lastIndexOf('}') + 1));
      if (obj['status'] === 'success') {
        $('#form').hide();
      } else {
        console.error(response)
      }
      $('#form-submit-result').html(obj['message']);
    });

    request.fail(function(_, textStatus, errorThrown) {
      // Log the error to the console
      console.error(
        'The following error occurred: ' +
        textStatus, errorThrown
      );
      $('#form-submit-result').html('Hiba történt. Kérjük keress minket az e-mail címünkön közvetlenül.');
    });

    // Callback handler that will be called regardless
    // if the request failed or succeeded
    request.always(function() {
      // Reenable the inputs
      inputs.prop('disabled', false);
    });
  }

  $('#form').submit(function(event) {
    event.preventDefault();
    submitForm();
  })


  // Smooth scroll.
  $(document).ready(function() {
    $("a[href*='#']").on('click', function(event) {
      // Make sure this.hash has a value before overriding default behavior
      if (this.hash !== '') {
        // Prevent default anchor click behavior
        event.preventDefault();

        // Store hash
        var hash = this.hash;

        // Using jQuery's animate() method to add smooth page scroll
        // The optional number (800) specifies the number of milliseconds it takes to scroll to the specified area
        $('html, body').animate({
          scrollTop: $(hash).offset().top
        }, 800, function() {

          // Add hash (#) to URL when done scrolling (default click behavior)
          window.location.hash = hash;
        });
      }
    });
  });
